const I18N = {
  current: 'en',
  langs: LANGS,
  dict: DICT,

  detect() {
    try {
      const stored = localStorage.getItem('cf-lang');
      if (stored && DICT[stored]) return stored;
    } catch {}
    const nav = (navigator.language || 'en').toLowerCase().split('-')[0];
    return DICT[nav] ? nav : 'en';
  },

  set(code, opts = {}) {
    if (!DICT[code]) return;
    this.current = code;
    try { localStorage.setItem('cf-lang', code); } catch {}
    const meta = LANGS.find(l => l.code === code);
    document.documentElement.setAttribute('lang', code);
    document.documentElement.setAttribute('dir', meta.dir);
    document.documentElement.setAttribute('data-lang', code);
    this.apply();
    document.dispatchEvent(new CustomEvent('langchange', { detail: { code, langMeta: meta } }));
  },

  t(key, vars = {}) {
    const fallback = DICT.en[key] || key;
    let s = (DICT[this.current] && DICT[this.current][key]) || fallback;
    if (vars && typeof s === 'string') {
      Object.keys(vars).forEach(k => { s = s.replace(`{${k}}`, vars[k]); });
    }
    return s;
  },

  apply(root = document) {
    root.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const txt = this.t(key);
      if (txt) el.textContent = txt;
    });
    root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.setAttribute('placeholder', this.t(key));
    });
    root.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria');
      el.setAttribute('aria-label', this.t(key));
    });
  },
};

window.I18N = I18N;
window.LANGS = LANGS;
